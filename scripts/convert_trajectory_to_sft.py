#!/usr/bin/env python3
"""
Convert DSGym trajectories to LLaMA-Factory ShareGPT format for SFT training.

This script reads trajectory files generated by DSGym's TrajectoryGenerator
and converts them to the ShareGPT format required by LLaMA-Factory.

Usage:
    python convert_trajectory_to_sft.py \
        --input-dir ./trajectory_results/qrdata_teacher_k5 \
        --output-file /data/fnie/LLaMA-Factory/data/qrdata_sft.json \
        --filter-correct \
        --min-turns 3 \
        --max-turns 12
"""

import json
import os
import re
from pathlib import Path
from typing import List, Dict, Any, Optional, Tuple
import argparse
from tqdm import tqdm
from collections import defaultdict


def load_trajectory(file_path: str) -> Dict[str, Any]:
    """Load a single trajectory file."""
    with open(file_path, 'r', encoding='utf-8') as f:
        return json.load(f)


def check_trajectory_correctness(trajectory: Dict[str, Any]) -> Tuple[bool, float]:
    """
    Check if trajectory has correct answer based on metrics.
    
    Returns:
        Tuple of (is_correct, score)
    """
    metrics = trajectory.get("metrics", {})
    
    # Check various metric names that indicate correctness
    correctness_metrics = [
        "exact_match", "fuzzy_exact_match", "list_match",
        "equivalence_by_llm", "fast_equivalence_by_llm"
    ]
    
    for metric_name in correctness_metrics:
        if metric_name in metrics:
            metric_value = metrics[metric_name]
            if isinstance(metric_value, dict):
                score = metric_value.get("score", 0)
            else:
                score = metric_value
            
            if score >= 0.99:
                return True, score
    
    # Check success field as fallback
    if trajectory.get("success", False):
        return True, 1.0
    
    return False, 0.0


def convert_to_sharegpt(
    trajectory: Dict[str, Any], 
    mask_execution: bool = False
) -> Optional[Dict[str, Any]]:
    """
    Convert DSGym trajectory to ShareGPT format.
    
    Args:
        trajectory: DSGym trajectory dict
        mask_execution: If True, add markers for execution output masking
    
    Returns:
        ShareGPT format dict with 'messages' key, or None if conversion fails
    """
    messages = []
    
    # Try different field names for conversation
    conversation = (
        trajectory.get("conversation") or 
        trajectory.get("trajectory") or 
        []
    )
    
    if not conversation:
        return None
    
    for turn in conversation:
        role = turn.get("role", "")
        content = turn.get("content", "")
        
        if not content:
            continue
        
        if role == "system":
            messages.append({"role": "system", "content": content})
        elif role == "user":
            messages.append({"role": "user", "content": content})
        elif role == "assistant":
            messages.append({"role": "assistant", "content": content})
        elif role == "observation":
            # Merge observation into user message for training
            messages.append({"role": "user", "content": content})
    
    if not messages:
        return None
    
    # Ensure conversation starts with system or user
    if messages[0]["role"] == "assistant":
        return None
    
    return {"messages": messages}


def extract_sample_key(trajectory: Dict[str, Any], file_name: str) -> str:
    """Extract unique sample identifier from trajectory or filename."""
    # Try to get from sample_id
    sample_id = trajectory.get("sample_id", "")
    if sample_id:
        # Remove trajectory suffix if present
        return re.sub(r'_traj_\d+$', '', sample_id)
    
    # Try to extract from filename
    # Pattern: prediction_X_traj_Y.json or conversation_sample_X_traj_Y.json
    match = re.search(r'(?:prediction|conversation_sample)_(\d+)', file_name)
    if match:
        return f"sample_{match.group(1)}"
    
    return file_name


def convert_directory(
    input_dir: str, 
    output_file: str,
    filter_correct_only: bool = True,
    select_best_per_sample: bool = True,
    min_turns: int = 3,
    max_turns: int = 15,
    verbose: bool = False
) -> Dict[str, Any]:
    """
    Convert all trajectories in directory to single SFT file.
    
    Args:
        input_dir: Directory containing trajectory JSON files
        output_file: Output path for combined SFT data
        filter_correct_only: Only include trajectories with correct answers
        select_best_per_sample: Select only the best trajectory per sample
        min_turns: Minimum turn count
        max_turns: Maximum turn count
        verbose: Print detailed information
    
    Returns:
        Dictionary with conversion statistics
    """
    input_path = Path(input_dir)
    
    # Find all JSON files
    json_files = list(input_path.glob("*.json"))
    if not json_files:
        # Try subdirectories
        json_files = list(input_path.glob("**/*.json"))
    
    print(f"Found {len(json_files)} trajectory files in {input_dir}")
    
    # Group trajectories by sample for best selection
    sample_trajectories = defaultdict(list)
    
    stats = {
        "total_files": len(json_files),
        "loaded": 0,
        "filtered_incorrect": 0,
        "filtered_turns": 0,
        "filtered_empty": 0,
        "conversion_errors": 0,
        "final_samples": 0,
        "unique_samples": 0
    }
    
    # Load and filter trajectories
    for file_path in tqdm(json_files, desc="Loading trajectories"):
        try:
            trajectory = load_trajectory(str(file_path))
            stats["loaded"] += 1
            
            # Check correctness
            is_correct, score = check_trajectory_correctness(trajectory)
            
            if filter_correct_only and not is_correct:
                stats["filtered_incorrect"] += 1
                continue
            
            # Check turn count
            turns = trajectory.get("turns", trajectory.get("total_turns", 0))
            if turns < min_turns or turns > max_turns:
                stats["filtered_turns"] += 1
                continue
            
            # Convert to ShareGPT format
            sft_sample = convert_to_sharegpt(trajectory)
            
            if not sft_sample or not sft_sample["messages"]:
                stats["filtered_empty"] += 1
                continue
            
            # Add metadata for best selection
            sample_key = extract_sample_key(trajectory, file_path.name)
            
            sample_trajectories[sample_key].append({
                "sft_sample": sft_sample,
                "score": score,
                "turns": turns,
                "file_name": file_path.name
            })
            
        except Exception as e:
            if verbose:
                print(f"Error processing {file_path}: {e}")
            stats["conversion_errors"] += 1
    
    stats["unique_samples"] = len(sample_trajectories)
    
    # Select best trajectory per sample (if enabled)
    sft_data = []
    
    for sample_key, trajectories in sample_trajectories.items():
        if select_best_per_sample and len(trajectories) > 1:
            # Sort by score (descending), then by turns (prefer shorter)
            trajectories.sort(key=lambda x: (-x["score"], x["turns"]))
            best = trajectories[0]
            sft_data.append(best["sft_sample"])
        else:
            # Add all trajectories
            for traj in trajectories:
                sft_data.append(traj["sft_sample"])
    
    stats["final_samples"] = len(sft_data)
    
    # Save output
    output_path = Path(output_file)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(sft_data, f, ensure_ascii=False, indent=2)
    
    # Print summary
    print(f"\n{'='*50}")
    print("Conversion Summary")
    print(f"{'='*50}")
    print(f"Total files found:     {stats['total_files']}")
    print(f"Successfully loaded:   {stats['loaded']}")
    print(f"Filtered (incorrect):  {stats['filtered_incorrect']}")
    print(f"Filtered (turns):      {stats['filtered_turns']}")
    print(f"Filtered (empty):      {stats['filtered_empty']}")
    print(f"Conversion errors:     {stats['conversion_errors']}")
    print(f"Unique samples:        {stats['unique_samples']}")
    print(f"Final SFT samples:     {stats['final_samples']}")
    print(f"{'='*50}")
    print(f"Output saved to: {output_file}")
    
    return stats


def main():
    parser = argparse.ArgumentParser(
        description="Convert DSGym trajectories to LLaMA-Factory SFT format",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  # Basic conversion with default settings
  python convert_trajectory_to_sft.py \\
      --input-dir ./trajectory_results \\
      --output-file ./sft_data.json

  # Include all trajectories (not just correct ones)
  python convert_trajectory_to_sft.py \\
      --input-dir ./trajectory_results \\
      --output-file ./sft_data.json \\
      --no-filter-correct

  # Keep all trajectories per sample (not just best)
  python convert_trajectory_to_sft.py \\
      --input-dir ./trajectory_results \\
      --output-file ./sft_data.json \\
      --all-trajectories
        """
    )
    
    parser.add_argument(
        "--input-dir", "-i", required=True,
        help="Input directory containing trajectory JSON files"
    )
    parser.add_argument(
        "--output-file", "-o", required=True,
        help="Output JSON file path for SFT data"
    )
    parser.add_argument(
        "--filter-correct", action="store_true", default=True,
        help="Only include trajectories with correct answers (default: True)"
    )
    parser.add_argument(
        "--no-filter-correct", action="store_true",
        help="Include all trajectories regardless of correctness"
    )
    parser.add_argument(
        "--all-trajectories", action="store_true",
        help="Keep all trajectories per sample (default: select best only)"
    )
    parser.add_argument(
        "--min-turns", type=int, default=3,
        help="Minimum turn count for inclusion (default: 3)"
    )
    parser.add_argument(
        "--max-turns", type=int, default=15,
        help="Maximum turn count for inclusion (default: 15)"
    )
    parser.add_argument(
        "--verbose", "-v", action="store_true",
        help="Print detailed error information"
    )
    
    args = parser.parse_args()
    
    # Handle filter flags
    filter_correct = args.filter_correct and not args.no_filter_correct
    select_best = not args.all_trajectories
    
    convert_directory(
        input_dir=args.input_dir,
        output_file=args.output_file,
        filter_correct_only=filter_correct,
        select_best_per_sample=select_best,
        min_turns=args.min_turns,
        max_turns=args.max_turns,
        verbose=args.verbose
    )


if __name__ == "__main__":
    main()
